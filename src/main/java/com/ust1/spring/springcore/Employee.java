package com.ust1.spring.springcore;

public class Employee {
	private int id;// press ctrl+1 gives the prompt
	private String name;
// ctrl+shift+f providing spaces
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
